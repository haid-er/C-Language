//Program to print different shapes using \n
#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
int main()
{
	printf("*******\n");
	printf("******\n");
	printf("*****\n");
	printf("****\n");
	printf("***\n");
	printf("**\n");
	printf("*\n\n\n\n");

	//First Shape C

	printf("CCCCCCCCC\n");
	printf("CC\n");
	printf("CC\n");
	printf("CC\n");
	printf("CCCCCCCCC\n\n\n\n");

	//Second Shape of +

	printf("    ++\n");
	printf("    ++\n");
	printf("++++++++++\n");
	printf("++++++++++\n");
	printf("    ++\n");
	printf("    ++\n\n\n\n");

	//Third Shape of T

	printf("++\n");
	printf("++\n");
	printf(" ++++++++++++\n");
	printf(" ++++++++++++\n");
	printf("++\n");
	printf("++\n\n\n\n");

	return 0;
}
